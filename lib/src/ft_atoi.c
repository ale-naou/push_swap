#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int nb;
	int is_negative;

	i = 0;
	nb = 0;
	is_negative = 0;
	while (str[i] == '\n' || str[i] == '\t' || str[i] == '\v'
			|| str[i] == '\r' || str[i] == '\f' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			is_negative = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - '0');
		i++;
	}
	return ((nb > 0 && is_negative ? -nb : nb));
}
